// Helper functions
export function getSiteById(id: string): ExtractionSite | undefined {
  return SITES.find(s => s.id === id);
}

export function getSitesByCountry(country: string): ExtractionSite[] {
  return SITES.filter(s => s.country === country);
}

export function getSitesByMaterial(materialId: string): ExtractionSite[] {
  return SITES.filter(s => s.materials.includes(materialId) || s.primaryMaterial === materialId);
}

export function getSitesByStatus(status: SiteStatus): ExtractionSite[] {
  return SITES.filter(s => s.status === status);
}

export function getSitesByScale(scale: SiteScale): ExtractionSite[] {
  return SITES.filter(s => s.scale === scale);
}

export function getMegaSites(): ExtractionSite[] {
  return SITES.filter(s => s.scale === 'mega');
}

